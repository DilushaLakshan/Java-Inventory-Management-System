    //load data to the table
    private void loadStockData(){
        try{
            clearStockData();
            ResultSet stockRS = dbConnection.execute("SELECT * FROM `stock` WHERE `product_code`='"+Integer.valueOf(lblProductID.getText())+"'");
            while(stockRS.next()){
                String id = String.valueOf(stockRS.getInt("id"));
                String manufactureDate = stockRS.getString("manufacture_date");
                String exp_date = stockRS.getString("exp_date");
                String quantity = String.valueOf(stockRS.getInt("quantity"));
                String sellingPrice = String.valueOf(stockRS.getDouble("selling_price"));
                String weight = String.valueOf(stockRS.getDouble("weight"));
                
                String tbData[] = {id,manufactureDate,exp_date,quantity,sellingPrice,weight};
                DefaultTableModel tblModel = (DefaultTableModel)tblStock.getModel();
                tblModel.addRow(tbData);
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }
    
