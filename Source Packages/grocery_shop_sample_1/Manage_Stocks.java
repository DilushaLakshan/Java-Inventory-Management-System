    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{
            if(txtQuantity.getText().isEmpty() == true){
                JOptionPane.showMessageDialog(rootPane, "Enter the product Quantity");
            }else if(txtSellingPrice.getText().isEmpty() == true){
                JOptionPane.showMessageDialog(rootPane, "Enter the selling price");
            }else if(txtWeight.getText().isEmpty() == true){
                JOptionPane.showMessageDialog(rootPane, "Enter the weight of one unit");
            }else{
                int quantity = Integer.valueOf(txtQuantity.getText());
                double sellingPrice = Double.valueOf(txtSellingPrice.getText());
                double weight = Double.valueOf(txtWeight.getText());
                
                ResultSet rs = dbConnection.execute("SELECT * FROM `stock` WHERE `product_code`='"+Integer.valueOf(lblProductID.getText())+"'");
                if(rs.next()){
                    ResultSet stockRS = dbConnection.execute("UPDATE `stock` SET `quantity`='"+quantity+"', `selling_price`='"+sellingPrice+"', `weight`='"+weight+"' WHERE `product_code`='"+Integer.valueOf(lblProductID.getText())+"'");
                    JOptionPane.showMessageDialog(rootPane, "Successfully Updated");
                    loadStockData();
                }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

