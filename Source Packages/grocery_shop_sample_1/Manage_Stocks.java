    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try{
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String manufactureDate = dateFormat.format(dtManufacture.getDate());
            String expDate = dateFormat.format(dtExpire.getDate());

            if(manufactureDate.isEmpty() == true){
                JOptionPane.showMessageDialog(rootPane, "Select the Manufacture Date");
            }else if(expDate.isEmpty() == true){
                JOptionPane.showMessageDialog(rootPane, "Select the Expire Date");
            }else if(txtQuantity.getText().isEmpty() == true){
                JOptionPane.showMessageDialog(rootPane, "Enter the product Quantity");
            }else if(txtSellingPrice.getText().isEmpty() == true){
                JOptionPane.showMessageDialog(rootPane, "Enter the selling price");
            }else if(txtWeight.getText().isEmpty() == true){
                JOptionPane.showMessageDialog(rootPane, "Enter the weight of one unit");
            }else{
                int quantity = Integer.valueOf(txtQuantity.getText());
                double sellingPrice = Double.valueOf(txtSellingPrice.getText());
                double weight = Double.valueOf(txtWeight.getText());
                
                ResultSet rs = dbConnection.execute("SELECT * FROM `stock` WHERE `product_code`='"+Integer.valueOf(lblProductID.getText())+"'");
                if(rs.next()){
                    JOptionPane.showMessageDialog(rootPane, "Stock for this product has been alredy added");
                }else{
                    ResultSet stockRS = dbConnection.execute("INSERT INTO `stock` (`manufacture_date`,`exp_date`,`quantity`,`selling_price`,`weight`,`product_code`) VALUES ('"+manufactureDate+"','"+expDate+"','"+quantity+"','"+sellingPrice+"','"+weight+"','"+Integer.valueOf(lblProductID.getText())+"')");
                    JOptionPane.showMessageDialog(rootPane, "Insertion Successful");
                    loadStockData();
                }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

